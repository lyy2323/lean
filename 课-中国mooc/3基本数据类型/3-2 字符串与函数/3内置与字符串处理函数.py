# 内置函数
print(abs(-10.22))  # 转化为正数，返回：10.22
print(chr(10010))  # 将Unicode编码转为字符，返回：✚
print(ord('✚'))  # 将字符转化为Unicode编码，返回：10010
print(complex(1,4))  # 复数，返回：(1，4j)
print(divmod(10,3))  # 模的结果，返回：(3，1)
print(eval("10+2"))  # 字符串统一为数值，返回：12
print(float(11))  # 浮点数显示，返回：11.0
print(hex(101))  # 转化为十六进制，返回：0x65
print(format(232))  # 将值格式化为字符串，返回：232
print(hex(10010))  # 将整数转化为十六进制小写形式字符串，返回：0x271a
print(oct(10010))  # 将整数转化为八进制的小写形式字符串，返回：0o23432
print(int(-111.12))  # 去掉小数点，以整数显示，返回：-111
print(round(-111.52))  # 四舍五入的去掉小数点，返回：-112
print(len('yyi.1 23'))  # 计数字符串的个数，返回：8
print(max(98,22,87,121))  # 提取最大的数值，返回：121
print(min(98,22,87,121))  # 提取最小的数值，返回：22
print(pow(10,3))  # 计算数值的幂，即10的3次方，返回：1000
print(pow(3,pow(2,2),6))  # 计算数值的幂，即2的2次方->(3,4,6)->3的4次方=81，然后取“81 % 6”的模，即81/6=13.5->81-13*6=3，所以返回：3
for i in range(22,25):  # 循环22~25的数值，返回：22 23 24
    print(i)  # 打印赋值后的i
print(str([1223]))  # 返回新的字符串形式，str(1.23)结果为”1.23“，str([1,2])结果为"[1,2]",返回：[1223]

# 字符串处理函数：

print('居中显示,宽度50'.center(50,'='))
print('用指定字符，拆分这句话，分开显示'.split('分'))
print('aBcDE'.lower())  # 统一返回为小写
print('aBcDE'.upper())  # 统一返回为大写
print('gg.dda,gg'.replace('gg','jealous'))
print('从两边剥去的函数'.strip('从数函'))
print('插入'.join('原来的句子放在这里'))
print('{:=^50,.2f}'.format(10000/3))  # {}槽说明：引导符（:）、填充、对齐、宽度、千位符、浮点数（.2f）