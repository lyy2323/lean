import turtle as t
t.setup(500,500)
t.penup()
t.fd(-100)
t.pendown()
t.pensize(10)
t.pencolor("blue")
t.fd(200)
t.left(120)
t.fd(200)
t.seth(240)
t.fd(200)
t.seth(0)
t.fd(100)
t.pencolor("red")
t.circle(60,360)
t.penup()
t.seth(90)
t.fd(100)
t.seth(180)
t.fd(20)
t.pendown()
t.circle(10,360)
t.seth(0)
t.penup()
t.fd(40)
t.pendown()
t.circle(-10,360)
t.seth(240)
t.penup()
t.fd(80)
t.seth(-20)
t.pendown()
for i in range(3):
    t.circle(10,40)
    t.circle(-10,40)
t.done()