print("halle world")
print("halle world".title())  # title是将英文第一个字母改为大写
print("halle world".upper())  # upper可以将对应的英文全部改为大写
print("halle world".lower())  # lower可以将对应的英文全部改为小写
print("halle \"world\"")  # 反斜杠是转义符
print("halle \nworld")  # 斜杠后面跟着n，是换行符
print("""三个引号可以自由换行
这是第一行
这是第二行
这是第三行""")

